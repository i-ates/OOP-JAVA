public class workers {
	private String name;
	private float salary;
	
	
	//constructor
	public workers(String name, float salary) {
		this.name = name;
		this.salary = salary;
	}

	
	//getter-setter
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public float getSalary() {
		return salary;
	}

	public void setSalary(float salary) {
		this.salary = salary;
	}
	
	
	
	
}
